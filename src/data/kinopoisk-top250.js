function randomTopMovie(randomIndex) {
  const index = typeof randomIndex === 'number'
    ? Math.max(0, Math.min(MOVIES.length - 1, Math.floor(randomIndex)))
    : require('crypto').randomInt(MOVIES.length);
  const label = MOVIES[index];
  const match = label.match(/^(.*)\s+\((\d{4})\)$/);
  const kinopoiskId = KINOPOISK_IDS[index];
  return {
    catalogIndex: index,
    title: match ? match[1] : label,
    year: match ? Number(match[2]) : null,
    label,
    kinopoiskId,
    kinopoiskUrl: `https://www.kinopoisk.ru/film/${kinopoiskId}/`,
    posterUrl: `https://st.kp.yandex.net/images/film_iphone/iphone360_${kinopoiskId}.jpg`
  };
}

